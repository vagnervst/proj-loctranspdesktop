package dao;

import model.DatabaseUtils;

public class AcessorioVeiculo extends DatabaseUtils {
	private String nome_tabela = "tbl_acessorioveiculo";
	private int id;
	private String nome;
	
	public String getNome_tabela() {
		return nome_tabela;
	}
	public void setNome_tabela(String nome_tabela) {
		this.nome_tabela = nome_tabela;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
}
